package Arrays_Declaration;

public class Demo02 {
    public static void main(String[] args) {
        boolean[] xr = new boolean[2];
        System.out.println(xr[0]+" , "+xr[1]);//false , false

        int[] yr = new int[2];
        System.out.println(yr[0]+" , "+yr[1]);//0 , 0

        double[] zr = new double[2];
        System.out.println(zr[0]+" , "+zr[1]);//0.0 , 0.0

        String[] pr = new String[2];
        System.out.println(pr[0]+" , "+pr[1]);//null , null

        char[] qr = new char[2];
        System.out.println(qr[0]+" , "+qr[1]);//nul , nul









    }
}
