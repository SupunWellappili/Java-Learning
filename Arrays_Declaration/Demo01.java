package Arrays_Declaration;

public class Demo01 {
    public static void main(String[] args) {
        int x;//A Variable Declaration
        x = 10;//A Variable initialization
        System.out.println(x);

        int[] xr;//An array Declaration
        xr = new int[7];//An Array Object
        xr[0] = 34;//An Array Initialization
        System.out.println(xr[6]);

    }
}
