package Controller.do_while;

public class Demo01 {
    public static void main(String[] args) {
        int i = 10;
        while(i<10){
            System.out.println("While loops Number : "+i);
        }

        int j=10;
        do{
            System.out.println("do/while loops Number : "+j);
        }while(j<10);

    }
}
